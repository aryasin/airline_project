void loadflights()
        
{
try
        {
            //drivers for os
           Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
           //one dsn points to one database
           Connection conn = 
                   DriverManager.getConnection("jdbc:odbc:project", "sa", "abc");
           
           Statement st = conn.createStatement();
           ResultSet rs = st.executeQuery("select * from flights");
           while (rs.next())
           {
               
               flightid =  rs.getString("fid").trim();
               flightName = rs.getString("fname");
               
               tmFlights.put(flightName, flightid);
               
               cmbflights.addItem(flightName);
              
           }

} catch(Exception ex)
{
    JOptionPane.showMessageDialog(null, ex);
    
}}

