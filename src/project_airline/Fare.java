void loadFaredetails()
    {
        try
        {
            //drivers for os
           Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
           //one dsn points to one database
           Connection conn=DriverManager.getConnection("jdbc:odbc:project", "sa","abc");
           
           Statement st = conn.createStatement();
           ResultSet rs = st.executeQuery("select fname,sname,seatstatusname,CustCategory,fare from flights,flightseatstatus,flightstatus,flightfare where flights.statusid=flightstatus.statusid and flightfare.fid=flights.fid and flightseatstatus.Seatid=flightfare.seatid ");
           ResultSetMetaData rsmt = rs.getMetaData();
           
           DefaultTableModel model = 
                   (DefaultTableModel) jTable1.getModel();
           
           Object rowdata[] = new Object[rsmt.getColumnCount()];
                     
           model.setNumRows(0);

           while (rs.next())
           {
               for(int i=1;i<=rsmt.getColumnCount();i++)
               {
                   rowdata[i-1] = rs.getString(i);
               }
               model.addRow(rowdata);
           }
           
           jTable1.setModel(model);
           
        }catch(Exception ex)
        {JOptionPane.showMessageDialog(null, ex);
            
        }}
